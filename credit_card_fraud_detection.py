## Threshold Tuning
from sklearn.metrics import f1_score

#using the best random forest probabilities
y_prob=best_rf.predict_proba(x_test)[:,1]
thresholds=np.linspace(0.1,0.9,50)
f1_scores=[]

for t in thresholds:
  y_pred_t=(y_prob >=t).astype(int)
  f1_scores.append(f1_score(y_test,y_pred_t))

best_threshold = thresholds[np.argmax(f1_scores)]

print("Best Threshold:",best_threshold)

## threshold comaparision
from sklearn.metrics import f1_score, precision_score, recall_score
y_prob=best_rf.predict_proba(x_test)[:,1]
for t in [0.6,0.7,0.9]:
  y_pred_t=(y_prob >= t).astype(int)

  print(f"Threshold:{t}")
  print("F1:",f1_score(y_test,y_pred_t))
  print("Precision:",precision_score(y_test,y_pred_t))
  print("Recall:",recall_score(y_test,y_pred_t))
  print("-"*30)

