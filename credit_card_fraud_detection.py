## Method 1:Random Undersampling (Simple)
from imblearn.under_sampling import RandomUnderSampler

rus=RandomUnderSampler(random_state=42)
x_res,y_res=rus.fit_resample(x_train,y_train)

"""(SMOTE) creates synthetic fraud samples, it increases fraud cases and keeps all original data

more realistic and better model performance
"""

## using SMOTE (Synthetic Minority Over-Sampling Technique)
from imblearn.over_sampling import SMOTE

smote=SMOTE(random_state=42)
x_res,y_res=smote.fit_resample(x_train,y_train)

pd.Series(y_res).value_counts()

x_train.shape

x_res.shape

#"Due to extreme class imbalance,Random Undersampling and smote were applied.
#SMOTE generated synthetic minority samples,improving model performance compared to under sampling"
## 1.Logistic Regression
from sklearn.linear_model import LogisticRegression

lr_model=LogisticRegression(max_iter=1000)
lr_model.fit(x_res,y_res)

## predictions
y_pred_lr=lr_model.predict(x_test)

## 2.Random Forest
from sklearn.ensemble import RandomForestClassifier

rf_model=RandomForestClassifier(n_estimators=100,random_state=42,n_jobs=-1)
rf_model.fit(x_res,y_res)

## Predictions
y_pred_rf=rf_model.predict(x_test)

"""EVALUATION START

"""

## Using Confusion Matrix
from sklearn.metrics import classification_report,confusion_matrix

## Logistic Regression Evaluation
print("Logistic Regression:\n")
